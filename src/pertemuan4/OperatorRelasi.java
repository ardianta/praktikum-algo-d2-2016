package pertemuan4;

public class OperatorRelasi {
    
    public static void main(String[] argumen){
        
        int umurRio = 25;
        int umurMira = 32;
        
        boolean perbandingan;
        
        // bandingkan umur, apakah umur Rio lebih besar dari Mira?
        perbandingan = umurRio > umurMira;
        
        System.out.println("Rio lebih tua dari Mira? " + perbandingan);
        
        
    }
    
}
